package hanhan.entity.goodsEntity.goodsID;
import java.util.ArrayList;
import java.util.List;
public class GoodsIDList {
	List<GoodsID>itemlist=new ArrayList<GoodsID>( );
	public GoodsIDList() {}
	public GoodsIDList(List<GoodsID> itemlist) {
		super();
		this.itemlist = itemlist;
	}
	public List<GoodsID> getItemlist() {
		return itemlist;
	}
	public void setItemlist(List<GoodsID> itemlist) {
		this.itemlist = itemlist;
	}
	@Override
	public String toString() {
		return "GoodsIDList [itemlist=" + itemlist + "]";
	}
	
}
