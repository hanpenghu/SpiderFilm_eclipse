package hanhan.entity.goodsEntity.suoYouShangPinZai31GeQuYuDeFenBieKuCun;

public class ProductAreaInventory {
	public ProductAreaInventory() {}
	/**
	 * 区域id,一个省份一个id
	 */
	String areaID;//
	/**
	 * /区域名字,就是省份名字
	 */
	String areaName;
	/**
	 * //区域库存,是个数字,就是这一区域(省份)存了多少货
	 */
	String areaInventory;
	/**
	 * //商品id,就是goodsID
	 */
	String productID;
	public String getAreaID() {
		return areaID;
	}
	public void setAreaID(String areaID) {
		this.areaID = areaID;
	}
	public String getAreaName() {
		return areaName;
	}
	public void setAreaName(String areaName) {
		this.areaName = areaName;
	}
	public String getAreaInventory() {
		return areaInventory;
	}
	public void setAreaInventory(String areaInventory) {
		this.areaInventory = areaInventory;
	}
	public String getProductID() {
		return productID;
	}
	public void setProductID(String productID) {
		this.productID = productID;
	}
	@Override
	public String toString() {
		return "ProductAreaInventory [areaID=" + areaID + ", areaName=" + areaName + ", areaInventory=" + areaInventory
				+ ", productID=" + productID + "]";
	}
	

}
